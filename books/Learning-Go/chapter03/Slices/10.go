package main

import "fmt"

func main() {
	x := []int{1, 2, 3, 4}
	y := x[:2] // [1, 2] --> [1, 20] --> [10, 20]
	z := x[1:] // [2, 3, 4] --> [20, 3, 4] --> [20, 30, 4]
	x[1] = 20 // [1, 20, 3, 4] --> [10, 20, 3, 4] --> [10, 20, 30, 4]
	y[0] = 10 // [10, 20]
	z[1] = 30 // [20, 30, 4]
	fmt.Println(x) // [10, 20, 30, 4]
	fmt.Println(y) // [10, 20]
	fmt.Println(z) // [20, 30, 4]
}